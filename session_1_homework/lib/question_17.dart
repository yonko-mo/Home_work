// If you need a variable that can hold any type of value and may change during
// execution, which data type would you use? Write a code example to show this.

// answer: i would use dynamic data type

// example:
void main() {
  dynamic value = 1;
  value = 10.5;
  print(value);
  value = "mo";
  print(value);
  value = true;
  print(value);
  value = false;
  print(value);
}
